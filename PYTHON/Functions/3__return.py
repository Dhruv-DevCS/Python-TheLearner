def add(x, y):
    z = x + y
    return z
    
def substract(x, y):
    z = x - y
    return z
def multiply(x, y):
    z = x * y
    return z
def divide(x, y):
    z = x / y
    return z

print(add(5,2))             # Prints Return Value, here It's z
print(substract(5,2))       # Prints Return Value, here It's z
print(multiply(5,2))        # Prints Return Value, here It's z
print(divide(5,2))          # Prints Return Value, here It's z